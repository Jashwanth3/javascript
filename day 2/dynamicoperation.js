function operation(a,b,op)
{
    if(op=='+')
    {
        console.log(a+b);
    }
    else 
    if(op=='-')
    {
        console.log(a-b);
    }
    else
    if(op=='/')
    {
        console.log(a/b);
    }
    else
    if(op=='%')
    {
        console.log(a%b);
    }
}
operation(1,2,'+');